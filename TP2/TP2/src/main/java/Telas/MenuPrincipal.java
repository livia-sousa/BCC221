    //botao que ira abrir nova tela para verificar o proprietario de um imovel
    private void BotaoVerificarPropActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoVerificarPropActionPerformed
        String nome = JOptionPane.showInputDialog("Digite o nome do proprietario");
        if(!(nome == null)){
            boolean saida = app.buscaProprietario(v, nome);
            if(saida)
                //informa se o proprietario se encontra na lista ou nao
                JOptionPane.showMessageDialog(null, "O proprietario se encontra na lista", "Busca sucedida", JOptionPane.INFORMATION_MESSAGE);
            else{
                JOptionPane.showMessageDialog(null, "O proprietario nao se encontra na lista", "Busca sucedida", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BotaoVerificarPropActionPerformed

