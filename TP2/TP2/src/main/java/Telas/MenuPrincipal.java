    //funcao que ira exportar um arquivo de saida
    private void BotaoExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoExportarActionPerformed
        try {
            saida = new Formatter("saida.txt");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível criar arquivo de saida", "ERRO!", JOptionPane.ERROR_MESSAGE);
            System.exit(1);
        }
        for(var E : v){
            saida.format(E.toString());
        }
        JOptionPane.showMessageDialog(null, "Arquivo criado com sucesso", "SUCESSO!", JOptionPane.INFORMATION_MESSAGE);
        saida.close();
    }//GEN-LAST:event_BotaoExportarActionPerformed

